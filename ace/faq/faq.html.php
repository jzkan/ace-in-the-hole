			<h2>FAQs</h2>
			<h3>What are the Rules?</h3>
			<p>We currently adhere to the USAT Rules for Triathlon &amp; Duathlon.  Important rules include no drafting, you must wear a helmet and music is <strong>ABSOLUTELY NOT</strong> allowed during the bike for obvious safety reasons.</p>

<h3>Can I use a personal music devise while cycling?</h3>
			<p>Absolutely no music devices are allowed during the bike segment for obvious safety risks and will result in immediate disqualification.</p>

<h3>Can I use a personal music devise while running?</h3>
			<p>During any running segments, we prefer for athletes to <strong>NOT</strong> use music devices to maximize safety, assure a fair competitive environment and assure athletes can hear all course marshal instructions. With that said, we also understand that many athletes rely on music to help endure the challenge of running. To accommodate the needs of our athletes while still assuring maximum safety and a fair, competitive environment, here is the arrangement that we can accommodate.</p>

    <p class="quote">If you chose to use a personal music device, you must always keep one ear open for instructions so only one ear-bud can be used at any time. In addition, if you opt to use music, you will not be eligible for awards, points and rankings. So as an athlete, you have to make a choiceÖmusic or awards/points/rankings.
    If you are using a personal music devise with two earbuds, you will be immediately disqualified. Trust us, we donít want to have to enforce a disqualification but in order to assure the safety of all athletes, this is a very important rule. We also request that if you chose to use a personal music device that you keep the volume to a minimum to assure all instructions can be heard.</p>


<h3>Do I need to wear a wetsuit?</h3>
			<p>No, you do not need to wear a wetsuit. Many will not wear a wetsuit while others will choose to wear a wetsuit because of the buoyancy and warmth factor.</p>
			<h3>Do I have to use a road or racing bike?</h3>
			<p>No. We welcome any type of bike as long as it is functioning properly with brakes and endcaps at the end of your handlebars.</p>
